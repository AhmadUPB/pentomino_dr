<h1>Pentomino Digital – DDI edition</h1>

<h2>Es gibt ein Problem! // There is problem!</h2>

<p>Die DDI Pentomino App verwendet moderne Webtechnologien, um auf einer Vielzahl von Geräten mit verschiedenen Eingabetechnologien nutzbar zu sein.
Die Voraussetzung dafür, dass das funktioniert ist ein moderner Browser. Leider mussten wir feststellen, dass der Browser, den Sie verwenden ein
zentrales Feature nicht erfüllt. Dieses Feature wurde bereits mit Der JavaScript-Version ES6 im Jahr 2015 spezifiziert und in damaligen Browsers umgesetzt.
Der von Ihnen verwendete Browser ist der Entwicklung also viele Jahre hinterher.</p>

<p>Die webbasierte Pentomino-App sollte auf allen aktuellen Browsern laufen. Sie wurde getestet in aktuellen Versionen von:</p>

<ul>
     <li>Google Chrome
     <li>Mozilla Firefox
     <li>Opera
     <li>Apple Safari
     <li>Microsoft Edge
</ul>


